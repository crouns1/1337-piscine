#include <unistd.h>

int	main(int c, char **v)
{
	int	i;
	int	j;

	i = 1;
	while (i < c)
	{
		j = 0;
		while (v[i][j] != 0)
		{
			write(1, &v[i][j], 1);
			j++;
		}
		write(1, "\n", 1);
		i++;
	}
}
